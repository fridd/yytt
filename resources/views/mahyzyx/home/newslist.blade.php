	<span style="font-size:16px;"><strong>2.学艺术？有什么用？</strong></span><br />
	<br />
	十几年前，或者二十几年前<br />
	经常会有人说：让孩子学艺术？能当饭吃吗？<br />
	这样的想法在以前可能没什么，但是在现在这样的大数据、人工智能愈加繁荣的大环境中，想要不被机器替代，唯有靠智慧的头脑和自身创造力。<br />
	&nbsp;<br />
	<strong>过去艺术不能当饭吃，</strong><strong>未来不学艺术就会没饭吃。</strong></span><br />
