	<strong>未来什么都有可能被替代，</strong><br />
	<strong>唯独艺术和娱乐不可能被替代！</strong><br />
	美术，是孩子情商提高、和完整人格形成的主要途径！更是一个人的造诣和人生高度的必备催化剂！<br />
	&nbsp;<br />
	学习艺术可以提高孩子的想象力、创造力、发散性思维。同时动手画画又是儿童最乐意接受的一种形式，所以，在幼儿到小学阶段进行艺术教育是非常必要的。以前的二十年，我们把人变成了机器；未来二十年，我们会把机器变成人。<br />
	&nbsp;过去的200年是知识、科技的时代；未来100年是智慧、体验、服务的时代。<br />
	知识可以学来的，但智慧是一种体验；<br />
	&nbsp;人类和机器未来的竞争，是智慧的竞争，体验的竞争；<br />
	<br />
	&nbsp;<br />
