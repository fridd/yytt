	<span style="font-size:16px;">&nbsp;<br />
	<strong>1.让孩子会&ldquo;玩&rdquo;很重要</strong></span><br />
	&nbsp;<br />
	阿里巴巴董事长马云在贵州峰会上的关于教育的大数据分享，再一次让人震惊！<br />
	他说：在未来三十年是最佳的超车时代，是重新定义的变革时代。<br />
	&nbsp;<br />
	如果我们继续以前的教学方法，对我们的孩子进行记、背、算这些东西，<strong>不让孩子去体验，不让他们去尝试琴棋书画，我可以保证，三十年后孩子们找不到工作。</strong><br />
	&nbsp;<br />
	因为他没有办法竞争过机器时代。<br />
	&nbsp;<br />
	<strong>知识可以学，但智慧不能学，只能体验。</strong>在未来，大数据、机器将把人类知识领域的事全部做完，人类和机器的竞争关键在于智慧在于体验&nbsp;。所以，让孩子会玩很重要！<br />
	马云还说：文化基本是玩出来的，死读书是可怕的学习，增加对生命感的体验，学习艺术不能当饭吃，但却是阳光空气和水。</span><br />
